function pathFor(id: PromptId): string {
  return join(PROMPTS_DIR, PROMPTS[id].filename);
}

/** Read the user's prompt file, materializing the default if missing. */
export function readPrompt(id: PromptId): { body: string; modified_at: string | null; isDefault: boolean } {
  const path = pathFor(id);
  if (!existsSync(path)) {
    writeFileSync(path, PROMPTS[id].defaultBody, "utf8");
  }
  const body = readFileSync(path, "utf8");
  const isDefault = body.trim() === PROMPTS[id].defaultBody.trim();
  const modified_at = (() => {
    try {
      return statSync(path).mtime.toISOString();
    } catch {
      return null;
    }
  })();
  return { body, modified_at, isDefault };
}

export function writePrompt(id: PromptId, body: string): void {
  if (body.length > 50_000) throw new Error("Prompt is too long (max 50,000 chars).");
  writeFileSync(pathFor(id), body, "utf8");
}

export function resetPrompt(id: PromptId): string {
  writeFileSync(pathFor(id), PROMPTS[id].defaultBody, "utf8");
  return PROMPTS[id].defaultBody;
}

/** Substitute `{{var}}` placeholders. Missing vars become empty strings. */
export function interpolate(template: string, vars: Record<string, string>): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_match, name) => vars[name] ?? "");
}
